fruits = ["Apple", "Banana", "Cherry", "Date", "Elderberry"]

# 2. Use a for loop to print each fruit with the message
#    Think about how to use 'fruit' (the variable that holds each item)
#    inside an f-string to get "I love Apple!", "I love Banana!", etc.
