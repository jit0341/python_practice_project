print("\n--- 6. Counting Elements (count()) ---")
items = ["apple", "banana", "apple", "orange", "banana", "apple"]

print(f"Count of 'apple': {items.count('apple')}")   # Output: Count of 'apple': 3
print(f"Count of 'banana': {items.count('banana')}") # Output: Count of 'banana': 2
print(f"Count of 'grape': {items.count('grape')}")   # Output: Count of 'grape': 0